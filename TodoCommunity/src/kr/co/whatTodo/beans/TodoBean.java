package kr.co.whatTodo.beans;

public class TodoBean {
	private int TODO_INDEX;
	private String TODO_PCODE;
	private String TODO_SUBJECT;
	private String TODO_CONTENT;
	private int TODO_WRITER_INDEX;
	private String TODO_FLAG;
	private String TODO_DATE;

	public int getTODO_INDEX() {
		return TODO_INDEX;
	}

	public void setTODO_INDEX(int tODO_INDEX) {
		TODO_INDEX = tODO_INDEX;
	}

	public String getTODO_PCODE() {
		return TODO_PCODE;
	}

	public void setTODO_PCODE(String tODO_PCODE) {
		TODO_PCODE = tODO_PCODE;
	}

	public String getTODO_SUBJECT() {
		return TODO_SUBJECT;
	}

	public void setTODO_SUBJECT(String tODO_SUBJECT) {
		TODO_SUBJECT = tODO_SUBJECT;
	}

	public String getTODO_CONTENT() {
		return TODO_CONTENT;
	}

	public void setTODO_CONTENT(String tODO_CONTENT) {
		TODO_CONTENT = tODO_CONTENT;
	}

	public int getTODO_WRITER_INDEX() {
		return TODO_WRITER_INDEX;
	}

	public void setTODO_WRITER_INDEX(int tODO_WRITER_INDEX) {
		TODO_WRITER_INDEX = tODO_WRITER_INDEX;
	}

	public String getTODO_FLAG() {
		return TODO_FLAG;
	}

	public void setTODO_FLAG(String tODO_FLAG) {
		TODO_FLAG = tODO_FLAG;
	}

	public String getTODO_DATE() {
		return TODO_DATE;
	}

	public void setTODO_DATE(String tODO_DATE) {
		TODO_DATE = tODO_DATE;
	}

}
